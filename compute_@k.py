def compute_all_metrics(
    df: pd.DataFrame,
    k_values: List[int],
    group_cols: List[str],
) -> pd.DataFrame:
    """Compute build@k, run@k, and pass@k for both regular and code_only pipelines."""

    status_counts = df.groupby(group_cols + ["status"]).size().unstack(fill_value=0).reset_index()
    status_counts.columns.name = None
    
    for s in ALL_STATUSES:
        if s not in status_counts.columns:
            status_counts[s] = 0
    #get counts for each status
    status_counts["total_samples"] = status_counts[ALL_STATUSES].sum(axis=1)
    
    #only one app, so just an app col, but need to reintroduce group_cols for later grouping
    app_col =  ["app"] + group_cols
    agg_df = df.groupby(app_col).agg(
        num_samples=("status", "count"),
        num_build=("did_build", "sum"),
        num_run=("did_run", "sum"),
        num_pass=("did_pass", "sum"),
    ).reset_index()
    
    for k in k_values:
        agg_df[f"build@{k}"] = agg_df.apply(lambda x: _passk(x["num_samples"], x["num_build"], k), axis=1)
        agg_df[f"run@{k}"]   = agg_df.apply(lambda x: _passk(x["num_samples"], x["num_run"], k), axis=1)
        agg_df[f"pass@{k}"]  = agg_df.apply(lambda x: _passk(x["num_samples"], x["num_pass"], k), axis=1)
        
    metric_cols = [c for c in agg_df.columns if "@" in c]
    # rads as build@k, run@k, pass@k order
    order_map = {"build": 0, "run": 1, "pass": 2}
    metric_cols = sorted(metric_cols, key=lambda x: (order_map.get(x.split("@")[0], 99), int(x.split("@")[1])))

    agg_dict = {metric: (metric, "mean") for metric in metric_cols} 
    agg_dict["apps"] = ("app", "nunique")
    
    metrics_df = agg_df.groupby(group_cols).agg(**agg_dict).reset_index()

    # merge status counts and metrics together
    result = pd.merge(status_counts, metrics_df, on=group_cols, how="outer")

    #
    ordered_cols = group_cols + ["apps", "total_samples"] + ALL_STATUSES + metric_cols
    ordered_cols = [c for c in ordered_cols if c in result.columns]

    return result[ordered_cols]
        


def main() -> None:
    args = get_args()
    input_file = Path(args.input_file).resolve()

    if not input_file.is_file():
        raise SystemExit(f"Error: {input_file} is not a file.")

    with input_file.open("r") as fh:
        data = json.load(fh)

    if not isinstance(data, list):
        raise SystemExit("Error: JSON file must contain a list of result objects.")

    if not data:
        raise SystemExit("Error: JSON file contains no entries.")

    print(f"Loaded {len(data)} entries from {input_file}")

    # Process JSON into structured standard pandas DataFrames
    df_reg, df_co, df_comb = json_to_dataframe(data)

    # Validate group-by columns exist
    for col in args.group_by:
        if col not in df_reg.columns:
            raise SystemExit(f"Error: group-by column '{col}' not found. Available: {list(df_reg.columns)}")

    # Compute merged metrics for each pipeline
    res_reg = compute_all_metrics(df_reg, args.k, args.group_by)
    res_co = compute_all_metrics(df_co, args.k, args.group_by)
    res_comb = compute_all_metrics(df_comb, args.k, args.group_by)

    # Display Results
    print("\n=== Regular Pipeline ===")
    print(res_reg.to_string(index=False))

    print("\n=== Code-Only Pipeline ===")
    print(res_co.to_string(index=False))

    print("\n=== Combined (Union Pool) Pipeline ===")
    print(res_comb.to_string(index=False))

    # Output CSVs
    if args.output:
        base_path = Path(args.output)
        stem = base_path.stem
        ext = base_path.suffix if base_path.suffix else ".csv"
        
        path_reg = base_path.with_name(f"{stem}_regular{ext}")
        path_co = base_path.with_name(f"{stem}_code_only{ext}")
        path_comb = base_path.with_name(f"{stem}_combined{ext}")

        res_reg.to_csv(path_reg, index=False)
        res_co.to_csv(path_co, index=False)
        res_comb.to_csv(path_comb, index=False)

        print(f"\nWrote CSVs to {base_path.parent}/")
        print(f"  - {path_reg.name}")
        print(f"  - {path_co.name}")
        print(f"  - {path_comb.name}")


if __name__ == "__main__":
    main()