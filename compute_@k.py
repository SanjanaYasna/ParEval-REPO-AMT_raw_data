def nCr(n: int, r: int) -> int:
    if n < r:
        return 1
    return comb(n, r)


def _passk(num_samples: int, num_correct: int, k: int) -> float:
    """Unbiased estimator of pass@k."""
    if num_samples < k:
        if num_samples == 0:
            return 0.0
        return float(num_correct > 0)
    if num_correct == 0:
        return 0.0
    if num_samples - num_correct < k:
        return 1.0
    return 1.0 - np.prod(1.0 - k / np.arange(num_samples - num_correct + 1, num_samples + 1))



def derive_flags(status: str) -> Dict[str, bool]:
    """
    From a status string, derive the three success flags:
      - did_build: not BUILD_FAIL and not INCORRECT_MODEL -> build@k
      - did_run:   VALIDATION_FAIL or PASS (MISSING COUNTS AGAINST IT) -> run@k
      - did_pass:  only PASS -> pass@k
    """
    return {
        "did_build": status not in ("BUILD_FAIL", "INCORRECT_MODEL"),
        "did_run": status in ("VALIDATION_FAIL", "PASS"),
        "did_pass": status == "PASS",
    }

def get_status_from_dict(status_dict):
    """ extract status from dict and represent as number (as multiple file names make key value pairs in the status list)"""
    if not status_dict:
        return "MISSING"

    priority = {
        "INCORRECT_MODEL": 0,
        "BUILD_FAIL": 1,
        "RUNTIME_FAIL": 2,
        "MISSING": 3,
        "VALIDATION_FAIL": 4,
        "PASS": 5,
    }
    #print(type(status_dict))
    #print("VALS", status_dict.values())
    return min(status_dict.values(), key=lambda x: priority[x])


def json_to_dataframe(data: List[Dict[str, Any]]) -> pd.DataFrame:
    """Convert JSON entries to a DataFrame with derived status columns for both
    regular (overall_status) and code_only (code_only_overall_status) and  both dfs."""
    regular_rows = [] 
    code_only_rows = []
    for entry in data:
        base_meta = {
            "app": entry.get("app", "unknown"),
            "prompt_strategy": entry.get("prompt_strategy", "unknown"),
            "llm_name": entry.get("llm_name", "unknown"),
            "source_model": entry.get("source_model", "unknown"),
            "dest_model": entry.get("dest_model", "unknown"),
            "output_number": entry.get("output_number", 0),
        }

        # regular pipeline
        reg_status_dict = entry.get("overall_status", {})
        #print("DICT", reg_status_dict, type(reg_status_dict))
        co_status_dict = entry.get("code_only_overall_status", {}) 
        reg_status = get_status_from_dict(reg_status_dict)
        reg_flags = derive_flags(reg_status)
        regular_rows.append({**base_meta, "status": reg_status, **reg_flags})

        #code_only pipeline 
        co_status_dict = entry.get("code_only_overall_status", {})
        co_status = get_status_from_dict(co_status_dict)
        co_flags = derive_flags(co_status)
        code_only_rows.append({**base_meta, "status": co_status, **co_flags})
    df_regular = pd.DataFrame(regular_rows)
    df_code_only = pd.DataFrame(code_only_rows) 
    df_both = pd.concat([df_regular, df_code_only], ignore_index =True)
    return df_regular, df_code_only, df_both


